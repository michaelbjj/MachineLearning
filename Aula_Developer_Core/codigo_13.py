# Calculdora cm Funções
def Calculadora(a, b, operacao):
    
    if operacao == 'Soma':
        return a + b
    elif operacao == 'Substr':
        return a - b
    elif operacao == 'Multi':
        return a * b
    elif operacao == 'Divi':
        return a / b
    else:
        return "Operação Inválida"
    
    print(Calculadora(10, 2, 'Soma'))
